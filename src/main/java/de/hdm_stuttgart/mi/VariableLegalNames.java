package de.hdm_stuttgart.mi;

public class VariableLegalNames {

    /**
     * Your application's main entry point.
     *
     * @param args Yet unused
     */
    public static void main( String[] args ) {

        // int for; // “for” is a Java keyword.
        int sum_of_data;
        int sumOfData;
        // int first-name; // Operators like “-” or “+” must not appear in variable names.
        int ABC;
        // int 42isThesolution; // Identifiers must not start with a number.
        int println; // println is a method's name but is no Java™ keyword.
        int B4;
        int AnnualSalary;
        // int "hello"; // String delimiters " must not be part of an identifier
        int _average;
        int ανδρος;
        int $sum;
    }
}
